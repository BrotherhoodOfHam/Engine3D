#include <tscore/system/memory.h>

using namespace ts;

///////////////////////////////////////////////////////////////////////////////////////////
//Allocators
///////////////////////////////////////////////////////////////////////////////////////////


void* operator new(size_t sz)
{
	return malloc(sz);
}
	
void* operator new[](size_t sz)
{
	return malloc(sz);
}
	
void operator delete(void* ptr)
{
	free(ptr);
}
	
void operator delete[](void* ptr)
{
	free(ptr);
}

